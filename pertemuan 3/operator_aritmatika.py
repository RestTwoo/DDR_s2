x = 5
y = 3

#penjumlahan 
print("penjumlahan =", x + y)

#pengurangan (-)
print("pengurangan =", x - y)

#perkalian (*)
print("perkalian =", x * y)

#pembagian (/)
print("pembagian =", x / y)

#modulus (%)
print("moodulus =", x % y)

#perpangkatan (**)
print("perpangkatan =", x ** y)

#pembagian bulat (//)
print("pembagiqan bulat =", x // y)